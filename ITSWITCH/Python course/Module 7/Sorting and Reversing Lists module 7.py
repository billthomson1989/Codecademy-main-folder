#Sorts the list from A-Z
z_fighters = ['Goku','Vegeta','Gohan','Piccolo','Trunks']
z_fighters.sort()
print(z_fighters)

#Sorts the list from Z-A
z_fighters = ['Goku','Vegeta','Gohan','Piccolo','Trunks']
z_fighters.sort(reverse=True)
print(z_fighters)

#Reverses the order of the list
z_fighters = ['Goku','Vegeta','Gohan','Piccolo','Trunks']
z_fighters.reverse()
print(z_fighters)

